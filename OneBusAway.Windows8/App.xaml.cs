namespace OneBusAway
{
    /// <summary>
    /// Provides application-specific behavior to supplement the default Application class.
    /// </summary>
    sealed partial class App : Application
    {
        /// <summary>
        /// Initializes the singleton application object.  This is the first line of authored code
        /// executed, and as such is the logical equivalent of main() or WinMain().
        /// </summary>
        public App()
        {
            // Inject the platform specific services into the service repository:
            ServiceRepository.FileService = new FileService();
            ServiceRepository.GeoLocationService = new GeoLocationService();
            ServiceRepository.NetworkService = new NetworkService();
            ServiceRepository.SettingsService = new SettingsService();

            this.InitializeComponent();
            this.Suspending += OnSuspending;
        }

        /// <summary>
        /// Invoked when the application is launched normally by the end user.  Other entry points
        /// will be used when the application is launched to open a specific file, to display
        /// search results, and so forth.
        /// </summary>
        /// <param name="args">Details about the launch request and process.</param>
        protected override void OnLaunched(LaunchActivatedEventArgs args)
        {
            Frame rootFrame = Window.Current.Content as Frame;

            if (rootFrame == null)
            {
                rootFrame = new Frame();
                Window.Current.Content = rootFrame;

                rootFrame.Navigate(typeof(MainPage), args.Arguments);
            }
            else
            {
                MainPage mainPage = rootFrame.Content as MainPage;
                mainPage.NavigateToPageControlByArguments(args.Arguments);
            }

            // Ensure the current window is active
            Window.Current.Activate();
        }        

        /// <summary>
        /// Invoked when application execution is being suspended.  Application state is saved
        /// without knowing whether the application will be terminated or resumed with the contents
        /// of memory still intact.
        /// </summary>
        /// <param name="sender">The source of the suspend request.</param>
        /// <param name="e">Details about the suspend request.</param>
        private void OnSuspending(object sender, SuspendingEventArgs e)
        {
            var deferral = e.SuspendingOperation.GetDeferral();
            //TODO: Save application state and stop any background activity
            deferral.Complete();
        }

        /// <summary>
        /// Invoked when the application is activated to display search results.
        /// </summary>
        /// <param name="args">Details about the activation request.</param>
        protected override void OnSearchActivated(Windows.ApplicationModel.Activation.SearchActivatedEventArgs args)
        {   
            var previousContent = Window.Current.Content;
            var frame = previousContent as Frame;

            // If the app does not contain a top-level frame, it is possible that this 
            // is the initial launch of the app. Typically this method and OnLaunched 
            // in App.xaml.cs can call a common method.
            if (frame == null)
            {
                frame = new Frame();
                Window.Current.Content = frame;

                frame.Navigate(typeof(MainPage), args.QueryText);
            }
            else
            {
                MainPage mainPage = frame.Content as MainPage;
                mainPage.NavigateToPageControlByArguments(args.QueryText);
            }

            Window.Current.Activate();
        }
    }
}
